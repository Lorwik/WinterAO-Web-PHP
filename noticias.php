	<h1 class="titulo text-center">¡Estrenamos Web!</h1>
	
	<p>Hace 3 meses, parte del antiguo Staff de WinterAO nos reunimos después de 8 años para emprender una nueva versión del proyecto. Si bien es cierto que para algunos la espera se hace larga
	desde el Staff podemos decir que estamos desarrollando la nueva versión en un tiempo récord. Y es que en 3 meses de desarrollo, hemos logrado hacer lo que en versiones anteriores nos llevo años hacer.</p>
	
	<p>Estamos desarrollando esta versión completamente desde cero, pero con la experiencia de las versiones anteriores, y con los conocimientos y herramientas actuales. Estamos poniendo mucho empeño para diseñar
	y desarrollar todo al mas mínimo detalle, aprendiendo de los errores pasados.</p>

	<p>Es por lo que nos enorgullece dar este gran paso abriendo la nueva Web y Wiki al publico, que si bien aun no están terminadas al 100%, es una buena señal de que estamos haciendo progresos y que cada día falta menos para esta online</p>

	<p>Le damos especialmente las gracias a Jopi por el increible trabajo que ha estado realizando en la web, y que a día de hoy sigue. También aprovechamos, para dar las gracias a toda la comunidad, que día tras día nos apoya y sigue creciendo.
	Y le recordamos que nos pueden seguir en las redes sociales, y unirse a nuestro canal de Discord para participar en la comunidad y estar al ultima sobre el desarrollo.</p>

